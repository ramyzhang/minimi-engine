TileMap::TileMap() {
    stone_ = renderer_->loadTexture("stone.png");
    water_ = renderer_->loadTexture("water.png");
    grass_ = renderer_->loadTexture("grass.png");
    
    loadMap(bgMap);
    
    srcRect_.x = srcRect_.y = 0;
    srcRect_.w = destRect_.w = 32;
    srcRect_.h = destRect_.h = 32;
}

void TileMap::loadMap(int newMap[20][25]) {
    for (int row = 0; row < 20; row++) {
        for (int col = 0; col < 25; col++) {
            tileMap_[row][col] = newMap[row][col];
        }
    }
}

void TileMap::renderMap() {
    for (int row = 0; row < 20; row++) {
        for (int col = 0; col < 25; col++) {
            destRect_.x = 32 * col;
            destRect_.y = 32 * row;
            
            switch (tileMap_[row][col]) {
                case 0:
                    renderer_->draw(grass_, &srcRect_, &destRect_);
                    break;
                
                case 1:
                    renderer_->draw(stone_, &srcRect_, &destRect_);
                    break;
                    
                case 2:
                    renderer_->draw(water_, &srcRect_, &destRect_);
                    break;
                    
                default:
                    printf("Not a valid tile option!\n");
                    break;
            }
        }
    }
}
